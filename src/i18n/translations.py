def get_translation(key: str, language: str = "en", **kwargs) -> str:
    """
    Get translated string for a key in the specified language.
    
    Args:
        key: Translation key (e.g., "welcome_title")
        language: Language code (en, hi, es, fr, de, zh, ml)
        **kwargs: Variables to format into the translation
    
    Returns:
        Translated and formatted string
    """
    # Get translations for the language, fallback to English
    translations = TRANSLATIONS.get(language, TRANSLATIONS["en"])
    
    # Get the translation, fallback to English if key not found
    text = translations.get(key, TRANSLATIONS["en"].get(key, key))
    
    # Format with any provided variables
    if kwargs:
        try:
            text = text.format(**kwargs)
        except KeyError:
            pass
    
    return text


